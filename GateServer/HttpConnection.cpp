unsigned char ToHex(unsigned char x){
	return  x > 9 ? x + 55 : x + 48;
}

unsigned char FromHex(unsigned char x)
{
	unsigned char y;
	if (x >= 'A' && x <= 'Z') y = x - 'A' + 10;
	else if (x >= 'a' && x <= 'z') y = x - 'a' + 10;
	else if (x >= '0' && x <= '9') y = x - '0';
	else assert(0);
	return y;
}

std::string UrlEncode(const std::string& str)
{
	std::string strTemp = "";
	size_t length = str.length();
	for (size_t i = 0; i < length; i++)
	{
		//判断是否仅有数字和字母构成
		if (isalnum((unsigned char)str[i]) ||
			(str[i] == '-') ||
			(str[i] == '_') ||
			(str[i] == '.') ||
			(str[i] == '~'))
			strTemp += str[i];
		else if (str[i] == ' ') //为空字符
			strTemp += "+";
		else
		{
			//其他字符需要提前加%并且高四位和低四位分别转为16进制
			strTemp += '%';
			strTemp += ToHex((unsigned char)str[i] >> 4);
			strTemp += ToHex((unsigned char)str[i] & 0x0F);
		}
	}
	return strTemp;
}

std::string UrlDecode(const std::string& str)
{
	std::string strTemp = "";
	size_t length = str.length();
	for (size_t i = 0; i < length; i++)
	{
		//还原+为空
		if (str[i] == '+') strTemp += ' ';
		//遇到%将后面的两个字符从16进制转为char再拼接
		else if (str[i] == '%')
		{
			assert(i + 2 < length);
			unsigned char high = FromHex((unsigned char)str[++i]);
			unsigned char low = FromHex((unsigned char)str[++i]);
			strTemp += high * 16 + low;
		}
		else strTemp += str[i];
	}
	return strTemp;
}

HttpConnection::HttpConnection(boost::asio::io_context& ioc) :_socket(ioc){

}

void HttpConnection::start() {
	auto self = shared_from_this();
	//读客户端发送来的 http get/post 请求 
	http::async_read(_socket, _buffer, _request, [self](beast::error_code ec,std::size_t bytes) {
		try {
			if (ec) {
				std::cout << "http read err is: " << ec.what() << std::endl;
				return;
			}
			boost::ignore_unused(bytes);
			self->HandleReq();      //处理请求
			self->CheckDeadline();  //超时检测        1. 处理请求、 2. 回复请求、 3. 超时检测 
		}
		catch(std::exception& ec){
			std::cout << "exception is: " << ec.what() << std::endl;
		}
		});
}

void HttpConnection::CheckDeadline() {
	auto self = shared_from_this();
	_deadline.async_wait([self](beast::error_code ec) {        //开启定时器，异步等待
		if (!ec) {       //没有错误，异步等到了，说明超时
			self->_socket.close();
		}
		});
}
void HttpConnection::WriteResponce() {
	auto self = shared_from_this();
	_response.content_length(_response.body().size());
	//回包：给客户端发送数据
	http::async_write(
		_socket,
		_response,
		[self](beast::error_code ec, std::size_t){
			//无论有没有发送成功都断开发送端（本端）连接，关闭本次定时器
			self->_socket.shutdown(tcp::socket::shutdown_send, ec);
			self->_deadline.cancel();
		});
}
void HttpConnection::HandleReq() {
	_response.version(_request.version());      //设置版本
	_response.keep_alive(false);                //设置为短链接

	if (_request.method() == http::verb::get) { //处理get请求
		PreParseGetParam();
		bool success = LogicSystem::getInstance().handleGet(_get_url, shared_from_this());//路由、连接
		if (!success) {                         //
			_response.result(http::status::not_found);
			_response.set(http::field::content_type, "text/plain");
			///回复数据: 往 body() 中写入数据
			beast::ostream(_response.body()) << "url not found\r\n";
			WriteResponce();
			return;
		}                                       //
		_response.result(http::status::ok);
		_response.set(http::field::server, "GateServer");
		WriteResponce();
		return;
	}else if (_request.method() == http::verb::post) { //处理post请求
		PreParseGetParam();
		bool success = LogicSystem::getInstance().handlePost(_request.target(), shared_from_this());//路由、连接
		if (!success) {                         //
			_response.result(http::status::not_found);
			_response.set(http::field::content_type, "text/plain");
			beast::ostream(_response.body()) << "url not found\r\n";
			WriteResponce();
			return;
		}                                       //
		_response.result(http::status::ok);
		_response.set(http::field::server, "GateServer");
		WriteResponce();
		return;
	}
}


//get 请求的参数解析
void HttpConnection::PreParseGetParam() {
	// 提取 URI  ：/get_test？key1 = value1 & key2 = value2
	auto uri = _request.target();  
	auto query_pos = uri.find('?');
	if (query_pos == std::string::npos) {                 //没找到“？”，即请求不带参数，整个就是 url
		_get_url = uri;
		return;
	}
	_get_url = uri.substr(0, query_pos);                  //前面 url 部分
	std::string query_string = uri.substr(query_pos + 1); //后面参数 Key:Value 部分
	std::string key;
	std::string value;
	size_t pos = 0;
	while ((pos = query_string.find('&')) != std::string::npos) {
		auto pair = query_string.substr(0, pos);
		size_t eq_pos = pair.find('=');
		if (eq_pos != std::string::npos) {
			key = UrlDecode(pair.substr(0, eq_pos)); // 假设有 url_decode 函数来处理URL解码  
			value = UrlDecode(pair.substr(eq_pos + 1));
			_get_params[key] = value;
		}
		query_string.erase(0, pos + 1);
	}
	// 处理最后一个参数对（如果没有 & 分隔符）  
	if (!query_string.empty()) {
		size_t eq_pos = query_string.find('=');
		if (eq_pos != std::string::npos) {
			key = UrlDecode(query_string.substr(0, eq_pos));
			value = UrlDecode(query_string.substr(eq_pos + 1));
			_get_params[key] = value;
		}
	}
}

tcp::socket& HttpConnection::getSocket() {
	return _socket;
}
